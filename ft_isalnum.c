#include "libft.h"

int	ft_isalnum(int n)
{
	if ((n >= 'a' && n <= 'z') || (n >= 'A' && n <= 'Z'))
		return (1);
	if (n >= '0' && n <= '9')
		return (1);
	return (0);
}
